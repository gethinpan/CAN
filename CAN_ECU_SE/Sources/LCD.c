//函数名：LCD_P8x16Str(byte x,byte y,byte *p)
//功能描述：写入一组标准ASCII字符串
//参数：显示的位置（x,y），y为页范围0～7，要显示的字符串
//返回：无
//==============================================================  
void LCD_P8x16Str(byte x,byte y,byte ch[])
{
  byte c=0,i=0,j=0;
        
  while (ch[j]!='\0')
  {    
    c =ch[j]-32;
    if(x>120){x=0;y++;}
    LCD_Set_Pos(x,y);    
  	for(i=0;i<8;i++)     
  	  LCD_WrDat(F8X16[c*16+i]);
  	LCD_Set_Pos(x,y+1);    
  	for(i=0;i<8;i++)     
  	  LCD_WrDat(F8X16[c*16+i+8]);  
  	x+=8;
  	j++;
  }
}


void char2hex(byte *ch, byte *hex,byte cnt)
{
	int i;
	int hbyte;
	for (i = 0; i < cnt; ++i)
	{
		hbyte = (ch[i / 2] >> (((i + 1) % 2) << 2)) & 0x0F;
		switch (hbyte)
		{
		case 0x00:
			hex[i] = '0'; break;
		case 0x01:
			hex[i] = '1'; break;
		case 0x02:
			hex[i] = '2'; break;
		case 0x03:
			hex[i] = '3'; break;
		case 0x04:
			hex[i] = '4'; break;
		case 0x05:
			hex[i] = '5'; break;
		case 0x06:
			hex[i] = '6'; break;
		case 0x07:
			hex[i] = '7'; break;
		case 0x08:
			hex[i] = '8'; break;
		case 0x09:
			hex[i] = '9'; break;
		case 0x0A:
			hex[i] = 'A'; break;
		case 0x0B:
			hex[i] = 'B'; break;
		case 0x0C:
			hex[i] = 'C'; break;
		case 0x0D:
			hex[i] = 'D'; break;
		case 0x0E:
			hex[i] = 'E'; break;
		case 0x0F:
			hex[i] = 'F'; break;
		}
	}
	hex[cnt-1] = '\0';
}

  
