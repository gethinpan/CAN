void LCD_WrDat(byte data)
{
	byte i=8;
	LCD_Data_Out &= ~LCD_CS;
	LCD_Data_Out |= LCD_CD;
  LCD_Data_Out&=~LCD_SCL;
  //asm("nop");    
  while(i--)
  {
    if(data&0x80){LCD_Data_Out|=LCD_SDA;}
    else{LCD_Data_Out&=~LCD_SDA;}
    LCD_Data_Out|=LCD_SCL; 
    asm("nop");
		//asm("nop");            
    LCD_Data_Out&=~LCD_SCL;    
    data<<=1;    
  }
	LCD_Data_Out |= LCD_CS; 
}
void LCD_WrCmd(byte cmd)
{
	byte i=8;
	
	LCD_Data_Out &= ~LCD_CS;
	LCD_Data_Out &= ~LCD_CD;
  LCD_Data_Out&=~LCD_SCL;
  //asm("nop");   
  while(i--)
  {
    if(cmd&0x80){LCD_Data_Out|=LCD_SDA;}
    else{LCD_Data_Out&=~LCD_SDA;}
    LCD_Data_Out|=LCD_SCL;
    asm("nop");
		//asm("nop");             
    LCD_Data_Out&=~LCD_SCL;    
    cmd<<=1;   
  } 	
	LCD_Data_Out |= LCD_CS; 
}
void LCD_Set_Pos(byte x, byte y)
{ 
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)|0x01); 
} 
void LCD_Fill(byte bmp_data)
{
	byte y,x;
	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(bmp_data);
	}
}
void LCD_CLS(void)
{
	byte y,x;	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10); 
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(0);
	}
}
